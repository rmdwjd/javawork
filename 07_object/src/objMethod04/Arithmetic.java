package objMethod04;

public class Arithmetic {
	//5칙연산 메소드 만들기
	int add (int x, int y) {
		int re = x+y;
		return re;
	}
	int subtract (int x, int y) {
		int re = x-y;
		return re;
	}
	int mult (int x, int y) {
		int re = x*y;
		return re;
	}
	double divide (int x, int y) {
		double re = (double)x/y;
		return re;
	}
	int remain (int x, int y) {
		int re = x%y;
		return re;
	}
	
	
	
	
}
